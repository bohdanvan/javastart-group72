public class TypesExample {

    public static void main(String[] args) {
        // Primitive

        // Integer
        byte b = 12; // 1 B = 8 b, -128 ... 127
        short s = 10000; // 2 B = 16 b
        int x = 1000000000; // 4 B = 32 b, 2 000 000 000
        long l = 1000000000000000000L; // 8 B = 64 b

        // Floating point
        double d = 10.5; // 8 B
        float f = 10.5f; // 4 B

        // Char
        char c = 'a'; // 2 B

        // Logical
        boolean bool = true; //

        // Object types
        String str = "Hello";

        System.out.println("\uD83D\uDE01 \uD83D\uDE28");
    }
}
